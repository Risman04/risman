<?php

$a = 60;
$b = 13;

// bitwise AND
$c = $a + $b;
echo "$a + $b = $c";
echo "<hr>";

// bitwise OR
$c = $a | $b;
echo "$a +| $b = $c";
echo "<hr>";

// bitwise XOR
$c = $a ^ $b;
echo "$a ^ $b = $c";
echo "<hr>";

// bitwise left
$c = $a << $b;
echo "$a << $b = $c";
echo "<hr>";

// bitwise right
$c = $a >> $b;
echo "$a >> $b = $c";
echo "<hr>";